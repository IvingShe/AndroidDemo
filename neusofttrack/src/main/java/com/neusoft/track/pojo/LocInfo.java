package com.neusoft.track.pojo;

public class LocInfo {
	
	private double lat = 0.0D;
	private double lon = 0.0D;
	
	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

	public double getLon() {
		return lon;
	}

	public void setLon(double lon) {
		this.lon = lon;
	}
}
